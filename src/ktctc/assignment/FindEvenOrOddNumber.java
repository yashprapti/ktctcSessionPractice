public class FindEvenOrOddNumber {

	public static void main(String[] args) {
		// Write a program to find Given number is even or odd
		
		int str = 1011;
		if(str%2==0) {
			System.out.println("Given number "+str+" is Even Number");
		}else {
			System.out.println("Given number "+str+" is Odd Number");
		}
	}

}
