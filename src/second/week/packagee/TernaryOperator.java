package second.week.packagee;

public class TernaryOperator {

	public static void main(String[] args) {
//		Ternary Operators are ?:
		// these operator returns same data which passed on expression
		// This if then else operator
		
		int age = 18;
		String msg = (age>=18)? "You can vote":"You Can not vote";
		// if condition is true then return "You can vote" else  return "You Can not vote"
		System.out.println(msg);

	}

}
